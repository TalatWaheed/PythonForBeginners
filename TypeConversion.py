#Type Conversion of Datatypes (Convert one datatype to another datatype)

a = 6
o = float(a)    #convert from int to float

print(o)
print(type(o))

print("**********")

n = 9.8
#convert from float to int

y = int(n)

print(y)
print(type(y))

print("**********")

l = 9

#convert from int to complex

w = complex(l)

print(w)
print(type(w))
