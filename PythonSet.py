#Python Set
#A set is an unordered, unique, immutable (cannot be changed) collection of items

#You can use same data type elements in the Set
myset = {"Orange","Red","Yellow","Green"}
print(myset)

set = {1,3,5,9}
print(set)

#You can use different data type in Set
set = {1,3,5,9,"Hey",7.9}
print(set)
